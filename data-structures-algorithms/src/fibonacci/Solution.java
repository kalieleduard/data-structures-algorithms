package fibonacci;

public class Solution {

    public long fib(int n) {
        long[] fibArray = new long[n + 2];
        fibArray[0] = 0;
        fibArray[1] = 1;

        for (int i = 2; i <= n; i++) {
            fibArray[i] = fibArray[i - 1] + fibArray[i - 2];
        }

        return fibArray[n];
    }
}
